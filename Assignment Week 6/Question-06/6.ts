let temperature="34C"

if(temperature <= "20C" ){
    console.log("Weather is extremely cold, so you should wear warm clothes");   
}
else if (temperature > "20C"  &&  temperature <= "30C"){
    console.log("Weather is moderate but you should take care of yourself");
}
else if (temperature >"30C"){
    console.log("Weather is hot, so you should wear light clothes");

}




